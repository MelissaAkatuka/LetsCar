-- INSERÇÃO
GO
CREATE PROCEDURE INSERCAO_CLIENTE
@NOME VARCHAR(100),
@TELEFONE BIGINT,
@EMAIL VARCHAR(50)
AS
BEGIN
    INSERT INTO CLIENTE( NOME, TELEFONE, EMAIL)
    VALUES(@NOME, @TELEFONE, @EMAIL);
END
GO

--EXCLUSÃO
GO
CREATE PROCEDURE EXCLUSAO_CLIENTE
@IDCLIENTE INT
AS
BEGIN
    DELETE FROM CLIENTE  WHERE IDCLIENTE  = @IDCLIENTE;
END
GO


--EDICAO
GO
CREATE PROCEDURE EDITAR_CLIENTE
@IDCLIENTE INT,
@NOME VARCHAR(100),
@TELEFONE BIGINT,
@EMAIL VARCHAR(50)
AS
BEGIN
    UPDATE CLIENTE
    SET
       @NOME = NOME,
       @TELEFONE = TELEFONE,
       @EMAIL = EMAIL
    WHERE
        @IDCLIENTE = IDCLIENTE
END
GO

--SELECT TODOS OS CLIENTES
GO
CREATE PROCEDURE SELECT_TODOS_CLIENTES
AS
BEGIN
    SELECT * FROM CLIENTE
END
GO

--SELECT TODOS OS FAVORITOS DOS CLIENTES
GO
CREATE PROCEDURE SELECT_FAVORITOS_CLIENTE
AS
BEGIN
    SELECT *
    FROM FAVORITO 
    INNER JOIN CLIENTE ON FAVORITO.IDFAVORITO = CLIENTE.IDCLIENTE;
END
GO






